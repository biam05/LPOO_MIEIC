public class Position {

    private int x, y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getx(){
        return x;
    }
    public int gety(){
        return y;
    }
    public void setx(int x_new)
    {
        x = x_new;
    }
    public void sety(int y_new)
    {
        y = y_new;
    }

}
